      <h2 class="section-title">The Front End Web Developer</h2>
      <section class="section-wrapper last">
        <h3 class="subtitle">What is a Front End Developer?</h3>
        <p class="section-text">For those not versed in digital technologies, front-end web development, also known as &ldquo;client-side&rdquo; development, is the exercise of producing HTML, CSS and JavaScript, the fundamental contributing and interacting technologies of a website or web application, in such a way that a user can see and interact with it directly. When you visit a website, the design elements you see were created by a front-end developer.</p>
        <p class="section-text">The goal of designing and developing a site is to ensure that when the user views it, they see the information arranged and formatted in a manner that is relevant and easy to read. The ultimate objective is to <i><strong>facilitate interaction</strong></i> between users and sites.</p>
        <h3 class="subtitle">Front End vs. Back End Development</h3>
        <p class="section-text">In contrast to front end development, back-end developers work to create the behind-the-scenes portions of a website or application, like data storage, security, site performance, or other server-side functions.</p>
        <p class="section-text">Back-end developers ensure the website performs correctly, focusing on databases, back-end business logic, application programming interfaces (APIs), architecture, and servers. They use code that helps browsers communicate with databases, store, understand, and delete data.</p>
        <h3 class="subtitle">Front End Developer vs Front End Designer</h3>
        <p class="section-text">Although they are generally considered to be two distinct disciplines, many people use the terms &ldquo;designer&rdquo; and &ldquo;developer&rdquo; interchangeably. To understand the distinction, it may be helpful to consider front end development as pertaining to <i><strong>how</strong></i> a website functions, while front end web design pertains to <i><strong>what</strong></i> the site is comprised of.</p>
        <p class="section-text">In actuality, there is often a blurring of the lines between the two, with developers often creating the artistic/aesthetic aspects of the site in addition to realizing it in code.</p>
        <h3 class="subtitle back-to-top-observer-target">Unique Challenges of a Front End Developer</h3>
        <p class="section-text">Most front end developers would currently agree that, the primary challenge of the discipline is that the tools and techniques used to create the front end of a website change <i><strong>constantly</strong></i> and <i><strong>very</strong></i> rapidly. Some have described staying up to date with the changes in the field as like &ldquo;trying to drink from a firehose&rdquo;. That may be an understatement. Nevertheless, the developer must constantly be aware of how the field is evolving.</p>
        <p class="section-text">Additionally, another of the most essential considerations in modern web development is the skill to seamlessly present content to users who view their site on a wide variety of devices with varying screen sizes and resolutions. Developers need to ensure that their site is presented clearly and consistently in different browsers, different operating systems, and different devices. The developer/designer must take into consideration these aspects when designing the site. Doing so requires very careful and precise planning and implementation.</p>
